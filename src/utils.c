#include "../minishell.h"

void	ft_putstr_fd_ms(char *s, int fd)
{
	int	i;

	i = 0;
	if (fd < 0 || s[i] == '\n')
		return ;
	while (s[i] != '\0')
	{
		write(fd, &s[i], 1);
		i++;
	}
	write(fd, "\n", 1);
}
